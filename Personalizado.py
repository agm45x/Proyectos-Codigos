import turtle

s=turtle.Screen()
t=turtle.Turtle()

s.bgcolor("green")
s.title("Proyecto 1")

t.shapesize(10,5,1)
t.shapesize(5,10,1)
t.shapesize(3,3,3)
t.fillcolor("Orange")
t.forward(100)
t.pencolor("white")
t.right(90)
t.forward(100)
s.bgcolor("gray")
t.color("green","blue")
t.right(90)
t.pensize(100)
t.forward(100)
turtle.done()